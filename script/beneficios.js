document.getElementById('imagen').addEventListener('click', function() {
    var imagen = document.getElementById('imagen');
    
    if (imagen.classList.contains('moved')) {
        imagen.classList.remove('moved');
    } else {
        imagen.classList.add('moved');
    }
});
